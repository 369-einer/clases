class Avion():
    def  __init__(self,apodo_nave,nro_asientos,ofrecimiento):
        self.apodo_nave=apodo_nave
        self.nro_asientos=nro_asientos
        self.ofrecimiento=ofrecimiento
    def acelerar(self,velocidad):
        self.velocidad+=velocidad
    def despegar_tiempo(self):
        return  self.despegar_tiempo

