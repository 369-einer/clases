class Adolesente():
    def __init__(self,edad,nombre,sexo,talla):
        self.edad=edad
        self.nombre=nombre
        self.sexo=sexo
        self.talla=talla
    def pensar(self,pensar):
        self.pensar=pensar
    def correr(self,correr):
        self.correr=correr
    def getNombre(self):
        return self.nombre
