class Machete():
    def __init__(self,material,espesor,tamaño,marca):
        self.material=material
        self.espesor=espesor
        self.tamaño=tamaño
        self.marca=marca
    def montar(self,montar):
        self.montar=montar
    def getNombre(self,nombre):
        return self.nombre
    def cortar(self,cortar):
        self.cortar=cortar

